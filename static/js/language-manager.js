    t(key) {
        return this.translations[this.currentLanguage][key] || key;
    }

    // translateæ–¹æ³•åˆ«åï¼Œä¸t()æ–¹æ³•åŠŸèƒ½ç›¸åŒ
    translate(key) {
        return this.t(key);
    }

