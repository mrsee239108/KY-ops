    init() {
        this.setupLanguageSelector();
        this.applyLanguage(this.currentLanguage);
    }

    // è®¾ç½®è¯­è¨€é€‰æ‹©å™¨
    setupLanguageSelector() {
        const languageSelect = document.getElementById('language-select');
        if (languageSelect) {
            languageSelect.value = this.currentLanguage;
            languageSelect.addEventListener('change', (e) => {
                this.changeLanguage(e.target.value);
            });
        }
    }

    // æ›´æ”¹è¯­è¨€
    changeLanguage(language) {
        this.currentLanguage = language;
        localStorage.setItem('system-language', language);
        this.applyLanguage(language);
    }

    // åº”ç”¨è¯­è¨€
    applyLanguage(language) {
        const translations = this.translations[language];
        if (!translations) return;

        // æ›´æ–°æ‰€æœ‰å¸¦æœ‰ data-i18n å±æ€§çš„å…ƒç´ 
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            if (translations[key]) {
                if (element.tagName === 'INPUT' && element.type === 'text') {
                    element.placeholder = translations[key];
                } else if (element.tagName === 'OPTION') {
                    element.textContent = translations[key];
                } else {
                    element.textContent = translations[key];
                }
            }
        });

        // æ›´æ–°æ‰€æœ‰å¸¦æœ‰ data-i18n-title å±æ€§çš„å…ƒç´ çš„title
        document.querySelectorAll('[data-i18n-title]').forEach(element => {
            const key = element.getAttribute('data-i18n-title');
            if (translations[key]) {
                element.title = translations[key];
            }
        });

        // æ›´æ–°æ‰€æœ‰å¸¦æœ‰ data-i18n-placeholder å±æ€§çš„å…ƒç´ çš„placeholder
        document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
            const key = element.getAttribute('data-i18n-placeholder');
            if (translations[key]) {
                element.placeholder = translations[key];
            }
        });

        // æ›´æ–°é¡µé¢æ ‡é¢˜
        document.title = translations['system-info'] || 'System Information';

        // æ›´æ–°HTML langå±æ€§
        document.documentElement.lang = language;

        // æ›´æ–°é¢åŒ…å±‘å¯¼èˆª
        this.updateBreadcrumb(language);
    }

